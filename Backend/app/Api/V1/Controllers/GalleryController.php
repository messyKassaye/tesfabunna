     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //

        $gallery = new Gallery();
        $gallery->src = $request->src;
        $gallery->width = $request->width;
        $gallery->height = $request->height;
        if($gallery->save()){
            return response()->json(['status'=>true,'message'=>'data is saved successfully']);
        }else{
            return  response()->json(['status'=>false,'message'=>'Data is not saved']);
        }
    }

